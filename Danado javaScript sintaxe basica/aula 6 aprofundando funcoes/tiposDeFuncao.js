// tipos de funcoes
// declarativas

function funcao(){
    console.log("Sou uma mensagem de uma funcao declarativa")
}
funcao()

// expressoes de funcoes sao funcoes atribuidas a expressoes. a nomeacao das funcoes por expressao e opcional ex1= var funcao - function nomeDaFuncao(){ ${ instrucao}} e ex2 = var funcao = function(){$ {instrucao};};

// com nomeacao

var funcao = function funcao(){
    console.log ("sou uma mensagem da funcao de expressao")
}
funcao()

// sem nomeacao

var funcao = function(){
    console.log ("sou uma mensagem da funcao de expressao")
}
funcao()

// Arrow Function =>. sao funcoes de expressao de sintaxe curta. Arrow functions sempre sao anonimas, e portanto nao podem ser nomeadas, devem ser declarada com parenteses(). seguido de => e depois Chaves {}

var funcao = () => {
    console.log( 'sou uma arrow function')
}
funcao();

